package com.testing.user;

/** Describes user model. */
public class User {

  private String name;

  /**
   * Set user name.
   *
   * @param name - user name.
   */
  public void setName(String name) {
    this.name = name;
  }

  /**
   * Get user name.
   *
   * @return user name.
   */
  public String getName() {
    return name;
  }
}
